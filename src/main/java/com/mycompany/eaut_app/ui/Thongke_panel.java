    private void btn_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_saveActionPerformed
        StringBuilder sb = new StringBuilder();
        DataValidator.validateEmpty(txt_masv, sb, "Vui lòng nhập mã sinh viên !");
        DataValidator.validateEmpty(txt_tienganh, sb, "Vui lòng nhập điểm sinh viên !");
        DataValidator.validateEmpty(txt_java, sb, "");
        DataValidator.validateEmpty(txt_thechat, sb, "");
        if (sb.length() > 0) {
            MessageDialogHelper.showErrorDialog(parentForm, sb.toString(), "Lỗi");
            return;
        }
        try {
            BangDiem bd = new BangDiem();
            bd.setMaSV(txt_masv.getText());
            bd.setTiengAnh(Float.parseFloat(txt_tienganh.getText()));
            bd.setLTHDT(Float.parseFloat(txt_java.getText()));
            bd.setGDTC(Float.parseFloat(txt_thechat.getText()));

            BangDiemDao dao = new BangDiemDao();
            if (dao.findByMaSv(txt_masv.getText()) != null) {
                if(MessageDialogHelper.showConfirmDialog(parentForm
                        , "Bạn có muốn cập nhật điểm không ", "Thông báo")== JOptionPane.NO_OPTION){
                    return;
                }
                if (dao.update(bd)) {
                    MessageDialogHelper.showMessageDialog(parentForm, "Bảng điểm đã cập nhật ", "Thông báo !");
                } else {
                    MessageDialogHelper.showMessageDialog(parentForm, "Không thể cập nhật", "Thông báo !");
                }
            } else {
                if (dao.insert(bd)) {
                    MessageDialogHelper.showMessageDialog(parentForm, "Bảng điểm đã được lưu ", "Thông báo !");
                } else {
                    MessageDialogHelper.showMessageDialog(parentForm, "Không thể lưu điểm", "Thông báo !");
                }
            }
            
            loadBangDiem();
        } catch (Exception e) {
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(parentForm, e.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_btn_saveActionPerformed

