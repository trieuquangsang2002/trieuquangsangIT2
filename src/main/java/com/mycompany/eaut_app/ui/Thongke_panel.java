    private void btn_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteActionPerformed
        try {
            if (txt_masv.getText().equals("")) {
                MessageDialogHelper.showMessageDialog(parentForm, "Cần phải nhập mã sinh viên", "Thông báo");
            }
            
            BangDiemDao dao = new BangDiemDao();
            BangDiem bd = dao.findByMaSv(txt_masv.getText());
            if (bd ==null) {
                MessageDialogHelper.showMessageDialog(parentForm, "Mã sinh viên không tồn tại", "Thông báo");
                return;
            }
            if(MessageDialogHelper.showConfirmDialog(parentForm, "Bạn có muốn xóa sinh viên không ?", "Thông báo")
                    == JOptionPane.NO_OPTION){
                    return;
             }
            if (dao.deleteByMaSv(txt_masv.getText())) {
                MessageDialogHelper.showMessageDialog(parentForm, "Điểm của sinh viên đã xóa thành công", "Thông báo");
            }else{
                MessageDialogHelper.showMessageDialog(parentForm, "Điểm của sinh viên xóa không thành công", "Thông báo");
            }
            loadBangDiem();
        } catch (Exception e) {
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(parentForm, e.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_btn_deleteActionPerformed


