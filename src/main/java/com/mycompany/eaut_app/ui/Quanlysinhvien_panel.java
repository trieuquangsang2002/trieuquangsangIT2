    private void btn_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_saveActionPerformed
        StringBuilder sb = new StringBuilder();
        DataValidator.validateEmpty(txt_masv, sb, "Mã sinh viên không được để trống");
        DataValidator.validateEmpty(txt_hoten, sb, "Họ tên không được để trống");
        if (sb.length() > 0) {
            MessageDialogHelper.showErrorDialog(parentForm, sb.toString(), "Lỗi");
            return;
        }
        try {
            SinhVien sv = new SinhVien();
            sv.setMaSV(txt_masv.getText());
            sv.setHoTen(txt_hoten.getText());
            sv.setEmail(txt_mail.getText());
            sv.setSDT(txt_sdt.getText());
            sv.setDiaChi(txt_diachi.getText());
            sv.setGIoiTinh(txt_gendermale.isSelected() ? 1 : 0);

            SinhVienDao dao = new SinhVienDao();
            if (dao.insert(sv)) {
                MessageDialogHelper.showMessageDialog(parentForm, "Sinh viên đã được lưu", "Thông báo");
                loadDataToTable();
            } else {
                MessageDialogHelper.showConfirmDialog(parentForm, "Sinh viên chưa được lưu thành công", "lỗi");
            }

        } catch (Exception e) {
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(parentForm,
                     e.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_btn_saveActionPerformed

