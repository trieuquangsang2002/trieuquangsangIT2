    private void btn_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteActionPerformed
        StringBuilder sb = new StringBuilder();
        DataValidator.validateEmpty(txt_masv, sb, "Mã sinh viên không được để trống");
        if (sb.length() > 0) {
            MessageDialogHelper.showErrorDialog(parentForm, sb.toString(), "Lỗi");
            return;
        }

        if (MessageDialogHelper.showConfirmDialog(parentForm,
                 "bạn có muốn xóa sinh viên không", "Hỏi") == JOptionPane.NO_OPTION) {
            return;
        }
        try {
            SinhVienDao dao = new SinhVienDao();
            if (dao.delete(txt_masv.getText())) {
                MessageDialogHelper.showMessageDialog(parentForm, "Sinh viên đã được xóa", "Thông báo");
                loadDataToTable();
                btn_newActionPerformed(evt);
            } else {
                MessageDialogHelper.showConfirmDialog(parentForm, "Sinh viên chưa được xóa", "lỗi");
            }

        } catch (Exception e) {
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(parentForm,
                    e.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_btn_deleteActionPerformed

    private void tbl_dataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_dataMouseClicked
        try {
            int row = tbl_data.getSelectedRow();

            if (row >=0) {
                String id = (String) tbl_data.getValueAt(row, 0);
                SinhVienDao dao = new  SinhVienDao();
                SinhVien sv = dao.findByid(id);

                if (sv != null) {
                    txt_masv.setText(sv.getMaSV());
                    txt_hoten.setText(sv.getHoTen());
                    txt_mail.setText(sv.getEmail());
                    txt_sdt.setText(sv.getSDT());
                    txt_diachi.setText(sv.getDiaChi());
                    txt_gendermale.setSelected(sv.getGIoiTinh()==1?true:false);

                }
            }

        } catch (Exception e) {
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(parentForm,
                e.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_tbl_dataMouseClicked


